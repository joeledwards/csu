class Relational 
{
    public static void main (String args[]) {
        int x = 7, y = 11, z = 11;
        System.out.println(x);
        System.out.println(y);
        System.out.println(x < y);
        System.out.println(x > z);
        System.out.println(y <= z);
        System.out.println(x >= y);
        System.out.println(y == z);
        System.out.println(x != z);
    }
}
