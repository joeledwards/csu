class FloatMath 
{
    public static void main (String args[]) {
        float x = 23.5F, y = 7.3F;
        System.out.println(x);
        System.out.println(y);
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
    }
}
